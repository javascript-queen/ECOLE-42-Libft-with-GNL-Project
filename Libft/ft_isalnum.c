#include "libft.h"

int	ft_isalnum(int symbol)
{
	if ((symbol >= 'a' && symbol <= 'z')
		|| (symbol >= 'A' && symbol <= 'Z') || (symbol >= '0' && symbol <= '9'))
		return (1);
	return (0);
}
